#include <unistd.h>

void	ft_rev_int_tab(int *tab, int size)
{
	int	x;
	int	y;
	int	r;

	x = 0;
	y = size - 1;
	while (x < size / 2)
	{
		r = tab[x]; 
		tab[x] = tab[y];
		tab[y] = r;
		x++;
		y--;
	}
}
